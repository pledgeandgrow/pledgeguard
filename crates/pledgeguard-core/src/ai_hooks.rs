//! AI tool hook integrations (goals 321-324).
//!
//! Provides hook scripts and configuration generators for AI coding tools
//! so that PledgeGuard scans prompts, file reads, and commands *before*
//! AI execution — preventing secrets from being sent to LLM APIs.
//!
//! ## Supported tools
//!
//! | Tool | Hook mechanism | What gets scanned |
//! |---|---|---|
//! | Cursor | `.cursor/hooks` pre-prompt hook | Prompt text + file contents |
//! | Claude Code | `.claude/hooks` pre-tool hook | Commands + file contents |
//! | GitHub Copilot | `.github/copilot-hooks` pre-suggestion hook | Inline suggestions context |
//! | OpenAI Codex | `.codex/hooks` pre-execution hook | Commands + file diffs |
//!
//! All hooks call `pledgeguard scan -` (stdin mode) with `--fail-on-findings`
//! so any detected secret blocks the AI action and prints a warning.

use std::fmt::Write;

/// Shell script header common to all hooks.
const HOOK_HEADER: &str = "#!/bin/sh\n# PledgeGuard AI hook — scans for secrets before AI execution.\n# Generated by `pledgeguard install-ai-hooks`.\n";

/// Returns the Cursor pre-prompt hook script.
///
/// Cursor invokes this hook before sending a prompt to the model.
/// The hook receives the prompt text on stdin; if any secret is found,
/// it exits non-zero to block the prompt.
pub fn cursor_hook() -> String {
    let mut s = String::from(HOOK_HEADER);
    s.push_str("# Cursor pre-prompt hook: scan prompt + attached file contents for secrets.\n");
    s.push_str("# If secrets are found, the prompt is blocked.\n");
    s.push_str("pledgeguard scan - --fail-on-findings --format json 2>/dev/null\n");
    s.push_str("exit $?\n");
    s
}

/// Returns the `.cursor/hooks.json` configuration snippet that registers
/// the pre-prompt hook.
pub fn cursor_config() -> String {
    r#"{
  "hooks": {
    "prePrompt": [
      {
        "command": ".cursor/hooks/pledgeguard-pre-prompt.sh",
        "timeout": 10000
      }
    ]
  }
}
"#
    .to_string()
}

