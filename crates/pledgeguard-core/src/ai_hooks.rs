/// All supported AI tools.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum AiTool {
    Cursor,
    ClaudeCode,
    Copilot,
    Codex,
}

impl AiTool {
    /// Returns the human-readable name.
    pub fn name(&self) -> &'static str {
        match self {
            AiTool::Cursor => "Cursor",
            AiTool::ClaudeCode => "Claude Code",
            AiTool::Copilot => "GitHub Copilot",
            AiTool::Codex => "OpenAI Codex",
        }
    }

    /// Returns the hook script content.
    pub fn hook_script(&self) -> String {
        match self {
            AiTool::Cursor => cursor_hook(),
            AiTool::ClaudeCode => claude_code_hook(),
            AiTool::Copilot => copilot_hook(),
            AiTool::Codex => codex_hook(),
        }
    }

    /// Returns the config file content.
    pub fn config_content(&self) -> String {
        match self {
            AiTool::Cursor => cursor_config(),
            AiTool::ClaudeCode => claude_code_config(),
            AiTool::Copilot => copilot_config(),
            AiTool::Codex => codex_config(),
        }
    }

    /// Returns the directory where the hook script should be installed
    /// (relative to the project root).
    pub fn hook_dir(&self) -> &'static str {
        match self {
            AiTool::Cursor => ".cursor/hooks",
            AiTool::ClaudeCode => ".claude/hooks",
            AiTool::Copilot => ".github/hooks",
            AiTool::Codex => ".codex/hooks",
        }
    }

    /// Returns the config file path (relative to the project root).
    pub fn config_path(&self) -> &'static str {
        match self {
            AiTool::Cursor => ".cursor/hooks.json",
            AiTool::ClaudeCode => ".claude/hooks.json",
            AiTool::Copilot => ".github/copilot-hooks.json",
            AiTool::Codex => ".codex/hooks.json",
        }
    }

    /// Returns the hook script filename.
    pub fn hook_filename(&self) -> &'static str {
        match self {
            AiTool::Cursor => "pledgeguard-pre-prompt.sh",
            AiTool::ClaudeCode => "pledgeguard-pre-tool.sh",
            AiTool::Copilot => "pledgeguard-pre-suggestion.sh",
            AiTool::Codex => "pledgeguard-pre-execution.sh",
        }
    }

    /// Returns all supported tools.
    pub fn all() -> &'static [AiTool] {
        &[
            AiTool::Cursor,
            AiTool::ClaudeCode,
            AiTool::Copilot,
            AiTool::Codex,
        ]
    }
}

/// Result of installing hooks for a single tool.
#[derive(Debug)]
pub struct HookInstallResult {
    pub tool: AiTool,
    pub hook_path: String,
    pub config_path: String,
    pub success: bool,
    pub error: Option<String>,
}

/// Installs hook scripts and config files for the specified AI tools
/// into the given project root directory. Creates directories as needed.
/// Returns a result for each tool.
pub fn install_hooks(root: &std::path::Path, tools: &[AiTool]) -> Vec<HookInstallResult> {
    tools
        .iter()
        .map(|&tool| install_one_hook(root, tool))
        .collect()
}

fn install_one_hook(root: &std::path::Path, tool: AiTool) -> HookInstallResult {
    let hook_dir = root.join(tool.hook_dir());
    let hook_path = hook_dir.join(tool.hook_filename());
    let config_path = root.join(tool.config_path());

    let result = (|| -> Result<(), String> {
        std::fs::create_dir_all(&hook_dir)
            .map_err(|e| format!("failed to create {}: {e}", hook_dir.display()))?;

        std::fs::write(&hook_path, tool.hook_script())
            .map_err(|e| format!("failed to write {}: {e}", hook_path.display()))?;

        #[cfg(unix)]
        {
            use std::os::unix::fs::PermissionsExt;
            if let Ok(meta) = std::fs::metadata(&hook_path) {
                let mut perms = meta.permissions();
                perms.set_mode(0o755);
                let _ = std::fs::set_permissions(&hook_path, perms);
            }
        }

        std::fs::write(&config_path, tool.config_content())
            .map_err(|e| format!("failed to write {}: {e}", config_path.display()))?;

        Ok(())
    })();

    HookInstallResult {
        tool,
        hook_path: hook_path.display().to_string(),
        config_path: config_path.display().to_string(),
        success: result.is_ok(),
        error: result.err(),
    }
}

/// Generates a summary of all installed hooks for display.
pub fn format_install_summary(results: &[HookInstallResult]) -> String {
    let mut out = String::new();
    for r in results {
        if r.success {
            let _ = writeln!(
                out,
                "  {} → {} + {}",
                r.tool.name(),
                r.hook_path,
                r.config_path
            );
        } else if let Some(ref err) = r.error {
            let _ = writeln!(out, "  {} → FAILED: {err}", r.tool.name());
        }
    }
    out
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_all_tools_have_scripts() {
        for &tool in AiTool::all() {
            let script = tool.hook_script();
            assert!(script.contains("pledgeguard scan"));
            assert!(script.contains("--fail-on-findings"));

            let config = tool.config_content();
            assert!(config.contains("hooks"));
        }
    }

    #[test]
    fn test_hook_dirs_are_distinct() {
        let dirs: Vec<_> = AiTool::all().iter().map(|t| t.hook_dir()).collect();
        let unique: std::collections::HashSet<_> = dirs.iter().collect();
        assert_eq!(dirs.len(), unique.len());
    }

    #[test]
    fn test_install_hooks_creates_files() {
        let tmp = tempfile::tempdir().unwrap();
        let results = install_hooks(tmp.path(), AiTool::all());
        assert_eq!(results.len(), 4);
        for r in &results {
            assert!(r.success, "{} failed: {:?}", r.tool.name(), r.error);
            assert!(std::path::Path::new(&r.hook_path).exists());
            assert!(std::path::Path::new(&r.config_path).exists());
        }
    }
}
