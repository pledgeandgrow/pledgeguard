/// Returns true if the path is inside a vendored dependency directory.
pub fn is_vendored_path(path: &Path) -> bool {
    let path_str = path.to_string_lossy();
    for dir in VENDOR_DIRS {
        let unix = format!("/{dir}/");
        let windows = format!("\\{dir}\\");
        let starts_unix = format!("{dir}/");
        let starts_windows = format!("{dir}\\");
        if path_str.contains(&unix)
            || path_str.contains(&windows)
            || path_str.starts_with(&starts_unix)
            || path_str.starts_with(&starts_windows)
        {
            return true;
        }
    }
    false
}

