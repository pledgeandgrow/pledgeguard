// ── 271: Binary file detection ──────────────────────────────────────────

/// Returns true if the content appears to be binary (contains null bytes
/// or a high proportion of non-text bytes).
pub fn is_binary_content(content: &[u8]) -> bool {
    if content.is_empty() {
        return false;
    }
    // Check for null bytes — strong binary indicator.
    if content.contains(&0u8) {
        return true;
    }
    // Check for UTF-8 validity.
    if std::str::from_utf8(content).is_err() {
        return true;
    }
    // Check proportion of non-printable characters.
    let sample_len = content.len().min(1024);
    let non_printable = content[..sample_len]
        .iter()
        .filter(|&&b| b < 0x20 && b != b'\n' && b != b'\r' && b != b'\t')
        .count();
    let ratio = non_printable as f64 / sample_len as f64;
    ratio > 0.30
}

// ── 272: Certificate file detection ─────────────────────────────────────

const CERT_EXTENSIONS: &[&str] = &["pem", "crt", "key", "cer", "der", "pfx", "p12", "jks"];

const CA_PATH_MARKERS: &[&str] = &["/etc/ssl/", "/etc/pki/", "/usr/local/share/ca-certificates/", "/usr/share/ca-certificates/", "ca-bundle", "ca-certificates"];

/// Returns true if the file is a certificate file in a known CA path.
pub fn is_ca_certificate_path(path: &Path) -> bool {
    let ext = path
        .extension()
        .and_then(|e| e.to_str())
        .map(|s| s.to_lowercase());
    if !ext.map(|e| CERT_EXTENSIONS.contains(&e.as_str())).unwrap_or(false) {
        return false;
    }
    let path_str = path.to_string_lossy();
    CA_PATH_MARKERS.iter().any(|m| path_str.contains(m))
}

