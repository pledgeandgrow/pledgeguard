// ── 264: .env file-aware scanning ───────────────────────────────────────

/// Parse .env file content and return lines that are not commented out.
/// Returns (line_number, line_content) pairs for active KEY=VALUE lines.
pub fn parse_env_active_lines(content: &str) -> Vec<(usize, String)> {
    let mut result = Vec::new();
    for (i, line) in content.lines().enumerate() {
        let trimmed = line.trim();
        if trimmed.is_empty() || trimmed.starts_with('#') {
            continue;
        }
        if !trimmed.contains('=') {
            continue;
        }
        result.push((i + 1, line.to_string()));
    }
    result
}

/// Returns true if the file path looks like a .env file.
pub fn is_env_file(path: &Path) -> bool {
    let name = path.file_name().and_then(|n| n.to_str()).unwrap_or("");
    name == ".env"
        || name.starts_with(".env.")
        || name.ends_with(".env")
        || name == ".envrc"
}

// ── 266: Documentation path detection ───────────────────────────────────

const DOC_MARKERS: &[&str] = &[
    "docs",
    "doc",
    "documentation",
    "README",
    "readme",
    "CHANGELOG",
    "changelog",
    "CONTRIBUTING",
    "contributing",
    "LICENSE",
    "license",
    "AUTHORS",
    "authors",
    "wiki",
    "help",
];

/// Returns true if the path appears to be in a documentation directory
/// or is a documentation file.
pub fn is_documentation_path(path: &Path) -> bool {
    let path_str = path.to_string_lossy();
    for marker in DOC_MARKERS {
        if path_str.contains(marker) {
            return true;
        }
    }
    if let Some(name) = path.file_name().and_then(|n| n.to_str()) {
        let lower = name.to_lowercase();
        if lower.ends_with(".md")
            || lower.ends_with(".rst")
            || lower.ends_with(".txt")
            || lower == "readme"
            || lower == "changelog"
            || lower == "license"
        {
            return true;
        }
    }
    false
}

