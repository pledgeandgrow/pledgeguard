/// Returns true if the file content contains a generated-file marker
/// within the first few lines.
pub fn is_generated_content(content: &str) -> bool {
    for line in content.lines().take(20) {
        for marker in GENERATED_MARKERS {
            if line.contains(marker) {
                return true;
            }
        }
    }
    false
}

/// Returns true if the file path suggests it's a generated file.
pub fn is_generated_path(path: &Path) -> bool {
    let path_str = path.to_string_lossy();
    let checks = [
        "/generated/", "\\generated\\", "generated/", "generated\\",
        "/.gen/", "/dist/", "\\dist\\", "dist/", "dist\\",
        "/build/", "\\build\\", "build/", "build\\",
        "/target/", "\\target\\", "target/", "target\\",
        "/.next/", "/out/", "\\out\\", "out/", "out\\",
    ];
    for c in checks {
        if path_str.contains(c) || path_str.starts_with(c) {
            return true;
        }
    }
    path_str.ends_with(".generated.go")
        || path_str.ends_with(".gen.go")
        || path_str.ends_with("_generated.go")
        || path_str.ends_with(".pb.go")
        || path_str.ends_with(".pb.dart")
        || path_str.ends_with(".g.dart")
}

